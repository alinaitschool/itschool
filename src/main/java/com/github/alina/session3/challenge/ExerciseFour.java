package com.github.alina.session3.challenge;

public class ExerciseFour {
    //counting up from 1 to 10
    public static void main(String[] args) {
        for(int i=0; i<10; i++){
            System.out.println(i);
        }
        for(int i=10; i>=1; i--){
            System.out.println(i);
        }
    }
}
