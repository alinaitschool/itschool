package com.github.alina.sesion4_challenge;
//Write a Java program where you declare an integer variable number with a value of 25. Use unary minus operator to change the sign of number and assign it to a variable negativeNumber. Print the values of number and negativeNumber.
public class ChallengeTwelve {
    public static void main(String[] args) {
        // Declare an integer variable number with a value of 25
        int number = 25;

        // Use unary minus operator to change the sign of number
        int negativeNumber = -number;

        // Print the values of number and negativeNumber
        System.out.println("Original number: " + number);
        System.out.println("Negative number: " + negativeNumber);
    }
}
